        <h2 class="fs-4">1. Carilah Tempat Magang Sesuai Passion Kamu</h2>
        <p>Meskipun magang hanya waktu sebentar, kamu tetap ga boleh main-main dalam memilih tempat magang lho! Sebisa mungkin kamu harus melamar di perusahaan yang sesuai dengan jurusan dan passion kamu. Kamu juga bisa mulai mencari perusahaan-perusahaan lewat social media seperti infomagang, infokerja, dll. Selain dari social media kamu juga bisa ke website yang menyediakan info magang seperti Jobstreet.com, jobplanet, dll. </p>

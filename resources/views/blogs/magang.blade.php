        <h2 class="fs-4">3. Pertimbangkan Lokasi Magang</h2>
        <p>Hal ini kamu harus perhatikan juga, sebisa mungkin kamu carilah lokasi magang yang dapat dijangkau dari rumah. Untuk kamu yang mahasiswa juga harus perhatikan jarak perusahaan magang kamu dan Universitas itu dapat dijangkau. Jika kamu memilih tempat yang jauh, nanti kamu akan kesulitan jika ada urusan mendadak atau kamu harus berangkat ke kampus dalam waktu dekat. </p>
        <h2 class="fs-4">4. Memilih Perusahaan Yang Peluang di Masa Depan Besar</h2>
        <p>Pilihlah perusahaan yang memiliki peluang besar di masa depan. Seperti yang kamu ketahui di era 4.0 ini, sebisa mungkin kamu harus memilih perusahaan yang familiar dengan teknologi. Jika kamu memiliki peforma yang baik saat magang pasti perusahaan akan merekrtut kamu sebagai karyawan tetap perusahaan. Lumayan bukan? </p>

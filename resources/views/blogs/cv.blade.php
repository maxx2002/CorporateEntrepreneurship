        <h2 class="fs-4">1. Ketahui Perusahaan Yang Hendak Akan Kamu Lamar</h2>
        <p>Kamu harus mengetahui latar belakang perusahaan yang akan kamu lamar terlebih dahulu. Tujuannya adalah untuk kamu bisa memberikan informasi-informasi yang lebih akurat & tepat di curriculum vitae kamu. Info-info yang penting ini yang berkaitan dengan jabatan yang kamu lamar juga. Jadi, jangan lupa untuk pelajari latar belakang perusahaan & posisi jabatan kamu ya!</p>
        <h2 class="fs-4">2. Resource Curriculum Vitae Yang Benar</h2>
        <p>Didalam Curriculum vitae kamu wajib ada beberapa poin seperti dibawah ini:</p>
        <ol>
            <li>Identitas Diri: Berisi tentang nama lengkap, nomor telepon, email, dan alamat tempat tinggal.</li>
            <li>Personality: Berisi tentang gambaran karakter diri.</li>
            <li>Pendidikan: Berisi tentang informasi mengenai pendidikan terakhir, IPK, maupun jurusan yang kamu tempuh.</li>
            <li>Ketermapilan: Berisi tentang keterampilan yang dimiliki.</li>
            <li>Pengalaman Kerja: Berisi tentang pengalaman kerja yang sudah dilalui sebelum melamar pekerjaan yang baru.</li>
            <li>Training atau Pelatihan: Berisi tentang macam-macam pelatihan yanng telah diikuti selama kurang lebih 3 tahun terakhir.</li>
            <li>Pencapaian: Berisi tentang pencapaian kamu selama kuliah, organisasi, kerja, atau apapun itu.</li>
            <li>Informasi lain: Berisi tentang berbagai hal yang bisa diunggulakn, mulai dari hobi, referensi, ataupun hal yang berhubungan dengan pekerjaann yang dilamar. </li>
        </ol>
        <h2 class="fs-4">3. Desain Curriculum Vitae Semenarik Mungkin</h2>
        <p>Kamu bisa desain Curriculum Vitae kamu diberbagai platform yang menyediakan berbagai template seperti canva. Desain Curriculum Vitae kamu semenarik mungkin tapi jangan terlalu berlebihan. Karena dengan desain juga perusahaan bisa melihat karakter seseorang. </p>
